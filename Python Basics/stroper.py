#declaration of index
myname="My name is Saurabh"

#to find which character is present at certain index 
print(myname[3])

#To find at certain index which character is present
print(myname.find("m"))

#to replace certain character with another 
print(myname.replace("y","e"))

#To check certain element how many times get repeated
print(myname.count("a"))

#to change whole string to uppercase
print(myname.upper())

#to change whole string to lowercase
print(myname.lower())

#To check length of String
print(len(myname))

